class Product{
    constructor(id, name, category, size, price, quantity, createAt, updatedAt) {
        this.id = id;
        this.name = name;
        this.category = category;
        this.size = size;
        this.price = price;
        this.quantity = quantity;
        this.createAt = createAt;
        this.updatedAt = updatedAt;
    }
}
export default Product